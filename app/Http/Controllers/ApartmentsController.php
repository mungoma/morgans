     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $apartment = new Apartment;
        $apartment->name = $request->input('name');
        $apartment->location = $request->input('location');
        $apartment->numberOfRentals=$request->input('rentals');
        $apartment->priceForEachRental=$request->input('prices');
        $apartment->imagePath=$request->input('image');
        $apartment->save();
        return redirect('/Apartments');
    }

